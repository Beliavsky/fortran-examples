!	To calculate the Fermi integral for k=-1/2 for real argument
!
!	Required routines : None
 
      FUNCTION FERMM05(X)
      IMPLICIT REAL*16(A-H,O-Z)
      DIMENSION A(9),B(10),A1(9),B1(10),A2(9),B2(10)
 
!	Coefficients of rational function approximations
      DATA A/  3.633183036310193702Q+00,  5.335383253918690131Q+00,
     1         4.079430052048247651Q+00,  1.747906909036781697Q+00,
     1         4.223506094680981781Q-01,  5.537045655151334865Q-02,
     1         3.607687989219176229Q-03,  9.850347648293245268Q-05,
     1         7.606021916949652951Q-07/
      DATA B/  1.772453850905514416Q+00,  5.186335126437444887Q+00,
     1         5.926527639536391534Q+00,  3.375396555770695396Q+00,
     1         1.017957982472663391Q+00,  1.604474074050487190Q-01,
     1         1.230542884288290881Q-02,  3.933239725620379548Q-04,
     1         3.650440948497754449Q-06,  9.742113007928913705Q-10/
 
      DATA A1/  2.380598901582346509Q-01,  1.948363117668486933Q-01,
     1          3.187787885725001684Q-02,  1.038176041451691336Q-02,
     1          9.406874895006179971Q-04,  1.135842036525309239Q-04,
     1          7.299137534044266613Q-06,  2.523032117352548804Q-07,
     1          9.717901643992206546Q-10/
      DATA B1/  1.072147023123106661Q+00,  9.289962788104335480Q-01,
     1          4.743603973028633253Q-01,  1.646743468291735872Q-01,
     1          3.969089819062670372Q-02,  6.726576928071186234Q-03,
     1          6.634259351526833300Q-04,  6.124666031703230112Q-05,
     1          3.150683483005771954Q-06,  4.772635816406201002Q-08/
 
      DATA A2/  3.702223911108869908Q+02,  2.598723392323324771Q+05,
     1          6.353495373811685919Q+07,  1.458761697561901484Q+10,
     1          1.726490095322560109Q+12,  1.400495505156701097Q+14,
     1          5.452693736065907221Q+15,  9.561693114496315040Q+16,
     1          4.481875529096675825Q+17/
      DATA B2/  2.000000000000001084Q+00,  7.396223151882119455Q+02,
     1          5.194366313824900668Q+05,  1.268547980181480670Q+08,
     1          2.912203205913661556Q+10,  3.440740530019870938Q+12,
     1          2.786232053682433961Q+14,  1.078287645942363898Q+16,
     1          1.861949241347277040Q+17,  7.588754613723405446Q+17/
 
 
      IF(X.LT.2.0) THEN
        Y=EXP(X)
        FN=((((((((B(10)*Y+B(9))*Y+B(8))*Y+B(7))*Y+B(6))*Y+B(5))*Y
     1             +B(4))*Y+B(3))*Y+B(2))*Y+B(1)
        FD=((((((((A(9)*Y+A(8))*Y+A(7))*Y+A(6))*Y+A(5))*Y+A(4))*Y+
     1             A(3))*Y+A(2))*Y+A(1))*Y+1
        FERMM05=Y*FN/FD
 
      ELSE IF(X.LT.10.0) THEN
        FN=((((((((B1(10)*X+B1(9))*X+B1(8))*X+B1(7))*X+B1(6))*X+
     1             B1(5))*X+B1(4))*X+B1(3))*X+B1(2))*X+B1(1)
        FD=((((((((A1(9)*X+A1(8))*X+A1(7))*X+A1(6))*X+A1(5))*X+A1(4))*X
     1             +A1(3))*X+A1(2))*X+A1(1))*X+1
        FERMM05=FN/FD
 
      ELSE
        Y=1./X**2
        FN=((((((((B2(10)*Y+B2(9))*Y+B2(8))*Y+B2(7))*Y+B2(6))*Y+B2(5))*Y
     1             +B2(4))*Y+B2(3))*Y+B2(2))*Y+B2(1)
        FD=((((((((A2(9)*Y+A2(8))*Y+A2(7))*Y+A2(6))*Y+A2(5))*Y+A2(4))*Y+
     1             A2(3))*Y+A2(2))*Y+A2(1))*Y+1
        FERMM05=SQRT(X)*FN/FD
      ENDIF
 
      END
