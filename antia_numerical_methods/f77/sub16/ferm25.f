!	To calculate the Fermi integral for k=5/2 for real argument
!
!	Required routines : None
 
      FUNCTION FERM25(X)
      IMPLICIT REAL*16(A-H,O-Z)
      DIMENSION A(9),B(9),A1(9),B1(10),A2(7),B2(8)
 
!	Coefficients of rational function approximations
      DATA A/  2.571894150057574794Q+00,  2.583310822362159194Q+00,
     1         1.297464965411635611Q+00,  3.478951634872978662Q-01,
     1         4.953536822731509544Q-02,  3.534319266081264567Q-03,
     1         1.114660054008188913Q-04,  1.196397340523770481Q-06,
     1         2.108653816687462637Q-09/
      DATA B/  3.323350970447843045Q+00,  8.253561418549546218Q+00,
     1         7.900830547729824064Q+00,  3.709901839066293724Q+00,
     1         9.037486960062833711Q-01,  1.118605468265125269Q-01,
     1         6.453871643985184629Q-03,  1.439223289367074978Q-04,
     1         7.851354785439239006Q-07/
 
      DATA A1/  3.143350423552492924Q-01,  1.185925514982136793Q-01,
     1          1.507118078811659113Q-02,  1.570482413916096774Q-03,
     1         -5.595169056590362619Q-05, -3.021530267629797655Q-06,
     1          2.935920822791014998Q-08, -3.139429619713453590Q-10,
     1          1.862374323772158779Q-12/
      DATA B1/  3.082574389659515354Q+00,  3.851032874650932823Q+00,
     1          2.542778697192629395Q+00,  1.123138726193956412Q+00,
     1          3.568215902085525392Q-01,  8.321216695003629528Q-02,
     1          1.368008186484687443Q-02,  1.461373661943904411Q-03,
     1          4.840746803030768445Q-05, -6.684259889599044773Q-06/
 
      DATA A2/ -1.010224365188074094Q+02,  6.300193167257369564Q+04,
     1         -2.611825140904720814Q+06,  6.096057209422390797Q+08,
     1         -1.021314648072490935Q+10,  7.043216447315414049Q+11,
     1         -6.058065587728390704Q+11/
      DATA B2/  2.857142857142856463Q-01, -2.475121812396447356Q+01,
     1          1.758866516117776321Q+04, -4.875066917911561303Q+05,
     1          1.636558505721404325Q+08, -4.202416018195836072Q+08,
     1          1.613941002540396296Q+11,  2.688699848072963231Q+12/
 
 
      IF(X.LT.2.0) THEN
        Y=EXP(X)
        FN=(((((((B(9)*Y+B(8))*Y+B(7))*Y+B(6))*Y+B(5))*Y
     1            +B(4))*Y+B(3))*Y+B(2))*Y+B(1)
        FD=((((((((A(9)*Y+A(8))*Y+A(7))*Y+A(6))*Y+A(5))*Y+A(4))*Y+
     1            A(3))*Y+A(2))*Y+A(1))*Y+1
        FERM25=Y*FN/FD
 
      ELSE IF(X.LT.10.0) THEN
        FN=((((((((B1(10)*X+B1(9))*X+B1(8))*X+B1(7))*X+B1(6))*X+
     1            B1(5))*X+B1(4))*X+B1(3))*X+B1(2))*X+B1(1)
        FD=((((((((A1(9)*X+A1(8))*X+A1(7))*X+A1(6))*X+A1(5))*X+A1(4))*X
     1            +A1(3))*X+A1(2))*X+A1(1))*X+1
        FERM25=FN/FD
 
      ELSE
        Y=1./X**2
        FN=((((((B2(8)*Y+B2(7))*Y+B2(6))*Y+B2(5))*Y
     1            +B2(4))*Y+B2(3))*Y+B2(2))*Y+B2(1)
        FD=((((((A2(7)*Y+A2(6))*Y+A2(5))*Y+A2(4))*Y+
     1            A2(3))*Y+A2(2))*Y+A2(1))*Y+1
        FERM25=X**3.5Q0*FN/FD
      ENDIF
 
      END
