!	To calculate the Fermi integral for k=3/2 for real argument
!
!	Required routines : None
 
      FUNCTION FERM15(X)
      IMPLICIT REAL*16(A-H,O-Z)
      DIMENSION A(9),B(10),A1(9),B1(10),A2(7),B2(8)
 
!	Coefficients of rational function approximations
      DATA A/  2.941809147359585304Q+00,  3.439655290706647512Q+00,
     1         2.056145556774501803Q+00,  6.754795354745318686Q-01,
     1         1.225806771429525260Q-01,  1.180305150936387470Q-02,
     1         5.504405550395816910Q-04,  1.038731891696509330Q-05,
     1         5.207633334112564377Q-08/
      DATA B/  1.329340388179136083Q+00,  3.675669313153495924Q+00,
     1         3.966435363691792986Q+00,  2.134338154956892860Q+00,
     1         6.096513425515978847Q-01,  9.154989439790675352Q-02,
     1         6.772074110214310551Q-03,  2.144157491371981044Q-04,
     1         2.129135670286070096Q-06,  2.031199981119644866Q-09/
 
      DATA A1/  3.063105631929247959Q-01,  1.417872685038368678Q-01,
     1          2.173189054734066272Q-02,  3.585800707573621106Q-03,
     1          1.142787213662411011Q-04,  8.044697088938151454Q-06,
     1          2.092114542066852857Q-07, -1.377485462439022186Q-09,
     1          7.580209448549789519Q-12/
      DATA B1/  1.152813011323656027Q+00,  1.370213448778108924Q+00,
     1          8.771659692101539598Q-01,  3.765170899492643514Q-01,
     1          1.157201605837170803Q-01,  2.543570598918036107Q-02,
     1          3.877159745588877839Q-03,  3.391356678944080395Q-04,
     1          8.185841691628457454Q-06,  7.929929143423822345Q-07/
 
      DATA A2/  4.240537001875903744Q+01,  7.106121449888719194Q+04,
     1          2.644247492013269539Q+06,  7.388974981953715144Q+08,
     1          8.693570648184380205Q+09,  6.684422318513495811Q+11,
     1          2.497458768271913493Q+11/
      DATA B2/  4.000000000000001628Q-01,  1.942954910777370651Q+01,
     1          2.852840658155141115Q+04,  1.233002624534097401Q+06,
     1          3.020327832136588749Q+08,  5.298507021546057868Q+09,
     1          2.882912998045199982Q+11,  1.741052221998644210Q+12/
 
 
      IF(X.LT.2.0) THEN
        Y=EXP(X)
        FN=((((((((B(10)*Y+B(9))*Y+B(8))*Y+B(7))*Y+B(6))*Y+B(5))*Y
     1            +B(4))*Y+B(3))*Y+B(2))*Y+B(1)
        FD=((((((((A(9)*Y+A(8))*Y+A(7))*Y+A(6))*Y+A(5))*Y+A(4))*Y+
     1            A(3))*Y+A(2))*Y+A(1))*Y+1
        FERM15=Y*FN/FD
 
      ELSE IF(X.LT.10.0) THEN
        FN=((((((((B1(10)*X+B1(9))*X+B1(8))*X+B1(7))*X+B1(6))*X+
     1            B1(5))*X+B1(4))*X+B1(3))*X+B1(2))*X+B1(1)
        FD=((((((((A1(9)*X+A1(8))*X+A1(7))*X+A1(6))*X+A1(5))*X+A1(4))*X
     1            +A1(3))*X+A1(2))*X+A1(1))*X+1
        FERM15=FN/FD
 
      ELSE
        Y=1./X**2
        FN=((((((B2(8)*Y+B2(7))*Y+B2(6))*Y+B2(5))*Y
     1            +B2(4))*Y+B2(3))*Y+B2(2))*Y+B2(1)
        FD=((((((A2(7)*Y+A2(6))*Y+A2(5))*Y+A2(4))*Y+
     1            A2(3))*Y+A2(2))*Y+A2(1))*Y+1
        FERM15=X**2.5Q0*FN/FD
      ENDIF
 
      END
